/*
Na expressão float *ptr;, o tipo float se refere ao tipo dos dados apontados pelo ponteiro.

ptr é uma variável do tipo "ponteiro para float".
O operador * indica que ptr é um ponteiro.
O tipo float indica que o ponteiro irá apontar para um valor do tipo float.
Ou seja: O que é do tipo float?
O valor no qual ptr aponta (ou seja, o dado referenciado por *ptr) é do tipo float.
*/